
namespace Person
{
    /// <summary>
    /// A class to represent a person.
    /// </summary>
    class Person
    {
        /// <summary>
        /// The name.
        /// </summary>
        public string Name { get; }


        /// <summary>
        /// A class to represent a person. 
        /// </summary>
        /// <param name="name">The name.</param>
        public Person(string name)
        {
            Name = name;
        }
    }
}
